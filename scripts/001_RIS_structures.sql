PROMPT ################# - WORKER - #################################

CREATE TABLE RIS.R_WORKER
(
PIN number(16) GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
NAME varchar2(128 CHAR),
SURNAME varchar2(128 CHAR),
V_NAME varchar2(128 CHAR),
V_SURNAME varchar2(128 CHAR),
DOB DATE,
EMAIL varchar2(288 CHAR),
M_F char(1 CHAR),
ID_WS number(16),
REG_DATE DATE,
STATUS char(1) DEFAULT 'A',
ID_TOFI number(12) INVISIBLE,
CONSTRAINT R_WR_PIN_PK_IDX PRIMARY KEY (PIN) USING INDEX TABLESPACE RIS_INDEX,
CONSTRAINT R_WR_WS_FK FOREIGN KEY (ID_WS)
	REFERENCES GSD.G_WORKER_STATUS(ID_WS),
CONSTRAINT R_WR_EMAIL_UQ UNIQUE (EMAIL),
CONSTRAINT R_WR_M_F_CHK CHECK (M_F IN ('M', 'F')),
CONSTRAINT R_WR_STATUS_CHK CHECK (STATUS IN ('A', 'D', 'W'))      
)
TABLESPACE RIS_DATA
;

ALTER TABLE RIS.R_WORKER RENAME CONSTRAINT R_WR_PIN_PK_IDX TO R_WR_PIN_PK;

COMMENT ON TABLE RIS.R_WORKER IS 'Workers';
COMMENT ON COLUMN RIS.R_WORKER.PIN IS 'PK - Personal Identification Number';
COMMENT ON COLUMN RIS.R_WORKER.NAME IS 'Name of Worker - Nominative';
COMMENT ON COLUMN RIS.R_WORKER.SURNAME IS 'Surname of Worker - Nominative';
COMMENT ON COLUMN RIS.R_WORKER.V_NAME IS 'Name of Worker - Vocative';
COMMENT ON COLUMN RIS.R_WORKER.V_SURNAME IS 'Surname of Worker - Vocative';
COMMENT ON COLUMN RIS.R_WORKER.DOB IS 'Date of Birth';
COMMENT ON COLUMN RIS.R_WORKER.EMAIL IS 'Email of Worker';
COMMENT ON COLUMN RIS.R_WORKER.M_F IS 'Male - Female';
COMMENT ON COLUMN RIS.R_WORKER.ID_WS IS 'FK - G_WS - Worker Status';
COMMENT ON COLUMN RIS.R_WORKER.REG_DATE IS 'Date of Registration';
COMMENT ON COLUMN RIS.R_WORKER.STATUS IS 'Record Status';

