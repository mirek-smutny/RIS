CREATE TABLE GSD.G_ORGANIZATION
(
OIN number(16),               
NAME varchar2(256 CHAR),
DOI date,
DOE date,
EMAIL varchar2(288 CHAR),
REG_DATE date,
STATUS char(1) DEFAULT 'A',
CONSTRAINT G_ORG_OIN_PK_IDX PRIMARY KEY (OIN) USING INDEX TABLESPACE GSD_INDEX,
CONSTRAINT G_ORG_EMAIL_UQ UNIQUE (EMAIL),
CONSTRAINT G_ORG_STATUS_CHK CHECK (STATUS IN ('A', 'D', 'W'))
)
TABLESPACE GSD_DATA;

ALTER TABLE GSD.G_ORGANIZATION RENAME CONSTRAINT G_ORG_OIN_PK_IDX TO G_ORG_OIN_PK;

COMMENT ON TABLE GSD.G_ORGANIZATION IS 'Organizations';
COMMENT ON COLUMN GSD.G_ORGANIZATION.OIN IS 'PK - Organization Identification Number';
COMMENT ON COLUMN GSD.G_ORGANIZATION.DOI IS 'Date of Inception';
COMMENT ON COLUMN GSD.G_ORGANIZATION.DOE IS 'Date of Expiration';
COMMENT ON COLUMN GSD.G_ORGANIZATION.EMAIL IS 'Email of Organization';
COMMENT ON COLUMN GSD.G_ORGANIZATION.REG_DATE IS 'Date of Registration';
COMMENT ON COLUMN GSD.G_ORGANIZATION.STATUS IS 'Record Status';

